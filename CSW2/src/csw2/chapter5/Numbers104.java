package csw2.chapter5;

public class Numbers104 {

	public static void main(String[] args) {
		
		Integer obj = Integer.valueOf(132);
		Double obj1 = Double.valueOf(542.369);
		Boolean obj2 = Boolean.valueOf(5==5);
		Character obj3 = Character.valueOf('J');
		
		System.out.println("Integer obj = "+obj);
		System.out.println("Double obj1 = "+obj1);
		System.out.println("Boolean obj2 = "+obj2);
		System.out.println("character obj3 = "+obj3);

	}

}
